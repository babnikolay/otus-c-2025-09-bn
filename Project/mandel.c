/*
Для каждой точки (пикселя) мы проверяем, как быстро последовательность z(n+1) = z(n)^2 + c превышает радиус 2.
Комплексные числа: Вместо специальной библиотеки используется две переменные a и b для вещественной и мнимой частей соответственно.
Формат PPM: Данные записываются в файл напрямую. После компиляции и запуска (gcc main.c -o mandel && ./mandel) в папке появится файл,
который визуализирует фрактал.
*/
#include <stdio.h>

int main()
{
    // Настройки изображения
    const int width = 800, height = 800;
    const int max_iter = 1000;
    FILE *fp = fopen("mandelbrot.ppm", "wb");

    // Заголовок PPM: формат P6 (бинарный), ширина, высота, макс. значение цвета
    fprintf(fp, "P6\n%d %d\n255\n", width, height);

    for (int y = 0; y < height; y++)
    {
        for (int x = 0; x < width; x++)
        {
            /*
            Масштабирование координат x и y в диапазон комплексной плоскости
            Область комплексной плоскости, которая визуализируется, задаётся прямоугольником:
            - по оси x от −2.0−2.0 до 0.50.5 (ширина 2.5)
            - по оси y от −1.25−1.25 до 1.251.25 (высота 2.5)
            */
            double ca = -2.0 + (x / (double)width) * 2.5;
            double cb = -1.25 + (y / (double)height) * 2.5;

            double a = 0, b = 0; // Начальное z = 0 (a + bi)
            int iter = 0;

            // Вычисление следующих значений комплексного числа z по формуле: z(n+1) = z(n)^2 + c
            while (a * a + b * b <= 4 && iter < max_iter)
            {                                       // (a+b)^2 = a^2 - b^2 + 2ab
                double next_a = a * a - b * b + ca; // новая действительная часть
                b = 2 * a * b + cb;                 // новая мнимая часть
                a = next_a;                         // обновляем a
                iter++;
            }

            // Простая раскраска: если точка внутри (iter == max_iter) — черная,
            // если снаружи — градиент серого
            unsigned char color = (iter == max_iter) ? 0 : (iter % 255);

            // Записываем RGB компоненты
            fputc(color, fp); // R
            fputc(color, fp); // G
            fputc(color, fp); // B
        }
    }

    fclose(fp);
    printf("Готово! Файл mandelbrot.ppm создан.\n");

    return 0;
}
